import { 数字转中文 } from "./序号转换工具.js";
export let 序号设置 = {
  默认设置: [
    //1级标题
    (序号) => {
      return `第${数字转中文(序号)}篇`;
    },
    //2级标题
    (序号) => {
      return `第${数字转中文(序号)}章`;
    },
    //3级标题
    (序号) => {
      return `第${数字转中文(序号)}节`;
    },
    //4级标题
    (序号) => {
      return `${数字转中文(序号)}、`;
    },
    //5级标题
    (序号) => {
        return `（${数字转中文(序号)}）`
    },
    //6级标题
    (序号) => {
        return `${序号}`
    },
  ],
};
